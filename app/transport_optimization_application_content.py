def references() -> list[str]:
    blocks = [h2("参考文献目录")]
    for index, item in enumerate(sorted(REF_CATALOG, key=lambda x: int(x.get("reference_number") or 9999)), 1):
        number = int(item.get("reference_number") or index)
        if item.get("reference_text"):
            text = str(item["reference_text"])
            if not text.startswith(f"[{number}]"):
                text = f"[{number}] {text}"
        else:
            authors = ", ".join(item.get("authors") or []) or "Anonymous"
            text = f"[{number}] {authors}. {item.get('title')}. {item.get('publisher') or ''}, {item.get('published_at') or ''}. {item.get('url') or ''}"
        blocks.append(text)
    blocks += [h2("引用与归档说明"),
               "参考文献条目来自本次Public Research Agent的查询计划和检索连接器结果。审计包保存connector_response.json、每条来源的raw/text/metadata文件、source_index.csv和manifest.json。正文引用编号通过source_id与PUBLIC_CLAIM关联，可验证URL、访问时间和SHA-256。"]
    return blocks


