def h2(text: str) -> str:
    return f"[[H2]]{text}"


def h3(text: str) -> str:
    return f"[[H3]]{text}"


def figure(filename: str, caption: str, width: float = 15.0) -> str:
    return f"[[FIGURE]]{(FIG / filename).as_posix()}|{caption}|{width}"


def table(headers: list[str], rows: list[list[str]]) -> str:
    lines = ["| " + " | ".join(headers) + " |", "|" + "|".join("---" for _ in headers) + "|"]
    lines += ["| " + " | ".join(str(c) for c in row) + " |" for row in rows]
    return "[[TABLE]]" + "\n".join(lines)


def cite(ids: list[int]) -> str:
    return "、".join(f"[{i}]" for i in ids)


def evidence_para(title: str, refs: list[int]) -> str:
    return f"《{title}》的设计与论证综合参考了{cite(refs)}。这些来源覆盖与本章相关的智能体规划、检索增强、多智能体协同、优化调度或治理安全方法；本章只吸收可迁移的公开方法和结论，不把文献中的实验条件、对象规模或性能数字直接视为本项目事实。"


