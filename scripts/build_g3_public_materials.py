from __future__ import annotations

import argparse
import json
import shutil
from pathlib import Path

from app.util import sha256_bytes, utc_now


SECTION_TITLES = [
    "项目摘要",
    "立项依据",
    "国内外研究现状",
    "关键科学问题",
    "研究目标",
    "研究内容",
    "关键技术",
    "技术路线",
    "实验方案",
    "创新点",
    "预期成果",
    "研究基础",
    "进度安排",
    "参考文献",
]


def _write(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text.strip() + "\n", encoding="utf-8")


def build(output_dir: Path) -> dict:
    if output_dir.exists():
        shutil.rmtree(output_dir)
    output_dir.mkdir(parents=True)

    _write(
        output_dir / "01_application_guide.md",
        """# 城市智能配送科研项目申报指南

## 申报方向
面向公开城市配送基准场景，研究动态任务、车辆状态、道路时变信息和服务时间窗共同作用下的协同优化与低扰动重规划方法。项目应形成可解释的关系模型、可复核的算法机制、公开可重复的实验方案和原型验证流程。

## 正文要求
申请书应包括项目摘要、立项依据、研究现状、关键科学问题、研究目标、研究内容、关键技术、技术路线、实验方案、创新点、预期成果、研究基础、进度安排和参考文献。研究目标、任务、方法、实验、指标和贡献必须形成一一对应关系。

## 证据与评价要求
公开研究应优先使用近五年的同行评议论文、综述和公开基准说明。每项创新应给出最接近工作、机制性局限、新增机制和可比较验证。实验至少覆盖静态规划、滚动重优化、局部修复和学习辅助策略四类基线，并报告可行率、目标值、响应时间和计划扰动率。

## 交付要求
最终交付包括完整申请书、公开来源归档、论证与章节映射、图表、DOCX、PDF、页面检查结果和可恢复的运行证据。不得把系统运行说明写入申请书主文。""",
    )

    _write(
        output_dir / "02_project_brief.md",
        """# 项目任务简表

## 项目名称
面向动态城市配送的关系感知协同优化与低扰动重规划方法研究

## 公开基准场景
研究对象是一个完全公开、无个人信息的城市配送基准：多个配送中心向公开虚拟客户点执行取送任务。任务具有容量、服务时间窗、优先级和前后置约束；车辆具有容量、可用时段和运行成本；道路旅行时间随公开事件流变化。系统只处理公开生成的基准数据。

## 总体问题
传统全量重优化能够获得较高全局质量，但在事件频繁到达时计算开销大且计划变化范围广；简单局部修复响应快，却可能造成后续约束传播和整体质量下降。本项目研究如何利用任务—资源—道路—时间窗关系图识别真实影响范围，在保持全局硬约束的条件下，只更新必要变量并控制计划扰动。

## 总体目标
建立统一关系模型和事件影响传播机制；提出全局可行性约束下的局部增量优化方法；形成面向不同事件强度的自适应求解策略；构建公开可重复的实验与原型验证环境。

## 研究周期与公开资源
周期为36个月。使用公开合成数据、公开道路网络和公开文献，不涉及真实人员、真实订单或非公开运行数据。

## 目标指标
1. 在预设公开基准上，生成方案的硬约束可行率达到100%。
2. 与每次事件后全量重优化相比，中等规模实例的重规划时间中位数降低不少于40%。
3. 在总成本相对全量重优化增加不超过5%的条件下，受事件影响后被修改的计划项比例降低不少于30%。
4. 事件影响范围识别的召回率不低于95%，以离线全量敏感性分析结果为参照。
5. 所有指标同时报告数据规模、事件分布、硬件环境、重复次数和置信区间；未达到目标时如实报告。

## 中心命题
若将动态配送事件表示为关系图上的约束变化，并以可验证的依赖传播确定最小候选影响子图，则可以在全局可行性校验下，用局部增量求解在响应速度、方案质量和计划稳定性之间取得可解释的折中。""",
    )

    _write(
        output_dir / "03_method_and_evaluation.md",
        """# 方法与实验输入

## 关系模型
核心对象包括任务、客户点、车辆、配送中心、道路边、时间窗、容量、服务顺序、事件、方案项和评价指标。关系包括分配、先行、互斥、容量占用、路径经过、时间依赖和事件影响。每条关系具有来源、版本和生效区间。

## 事件影响传播
事件类型包括新任务到达、任务取消、车辆不可用、道路旅行时间变化和服务时间窗调整。影响传播从发生变化的对象出发，沿硬约束依赖边扩展；当边界变量的可行域和代价下界均未变化时停止。传播结果形成候选影响子图，并保留传播路径供复核。

## 增量优化
冻结影响子图之外的计划变量。对子图内部变量构建局部混合整数或约束规划模型，并增加计划变更惩罚。若局部模型不可行，则按已记录的边界冲突逐层扩展子图；超过预算后退化到全量重优化。每次输出都经过全局硬约束校验。

## 自适应策略
根据事件强度、初始影响子图规模、约束紧度和历史扩展次数，在规则局部修复、局部精确优化和全量重优化之间选择。学习模型只能预测求解策略，不能直接绕过可行性校验。

## 实验数据
公开生成三档规模：小规模50个客户和8辆车；中规模200个客户和30辆车；大规模500个客户和80辆车。每档生成至少30个随机种子。事件流分别设置低、中、高三档强度，并保存生成器版本和种子。

## 对照方法
B0为事件后全量重优化；B1为贪心插入与删除修复；B2为固定半径邻域重优化；B3为滚动时间窗重优化；B4为本项目的关系感知自适应增量优化。

## 指标与统计
硬约束可行率按通过独立校验器的运行次数占比计算；响应时间使用相同硬件上的墙钟时间；质量差距以相对B0目标值计算；计划扰动率为发生变化的计划项数除以事件前计划项总数；影响范围召回率以离线全量敏感性分析确定的真实受影响变量集为参照。每项实验至少重复30次，报告中位数、四分位间距、均值和95%置信区间。

## 消融实验
分别移除关系类型、停止准则、计划变更惩罚和自适应策略，观察响应时间、质量差距和扰动率变化。通过失败案例分析记录局部模型不可行、影响子图扩展和全量退化的原因。""",
    )

    _write(
        output_dir / "04_foundation_and_schedule.md",
        """# 研究基础与实施条件

## 已有公开基础
基准团队已经完成一个公开的Python离散事件仿真骨架、一个可读取标准车辆路径实例的约束校验器，以及一个调用开源求解器的静态配送优化原型。当前原型能够在小规模静态实例上生成可行方案，但尚未实现事件影响传播、增量模型构建和计划扰动评价。上述状态作为本次公开能力基准的已确认输入，不代表真实机构成果。

## 可用条件
运行环境包含Python、SQLite、开源优化工具、LibreOffice、Chromium和Poppler。实验数据由公开生成器产生，代码、参数、种子、日志和结果表均可归档。公开研究只访问学术元数据或公开网页。

## 工作包
WP1完成关系模型、数据生成器和独立可行性校验器；WP2完成事件影响传播和停止准则；WP3完成局部增量模型、扩展策略和全量退化机制；WP4完成自适应策略、公开实验和原型集成；WP5完成复现实验、论文和软件文档。

## 进度
第1—6个月完成数据与关系模型；第7—14个月完成影响传播；第15—22个月完成增量优化；第23—28个月完成自适应策略；第29—34个月完成多规模实验；第35—36个月完成成果整理和公开复核。

## 风险与应对
若影响传播召回不足，则扩大关系类型并使用离线敏感性分析校准；若局部模型频繁不可行，则调整边界条件和扩展策略；若大规模求解超时，则使用分层邻域和时间预算；若目标指标未达到，则保留完整负结果并分析适用边界。""",
    )

    _write(
        output_dir / "05_public_research_requirements.md",
        """# 公开研究任务

## 研究问题
1. 近五年动态车辆路径、在线配送和重优化方法采用了哪些可比较基线，其主要局限是什么？
2. 增量优化、局部搜索和大邻域搜索如何控制重规划范围与响应时间？
3. 计划稳定性、解扰动和动态响应的评价指标如何定义？
4. 图关系建模或图学习在组合优化中的作用和适用边界是什么？

## 检索要求
查询必须同时包含具体问题、方法类型、基线或局限词以及2021—2026时间范围。至少覆盖动态车辆路径综述、重优化或大邻域搜索、计划稳定性指标和图增强组合优化四类来源。优先同行评议论文和高质量综述。

## 使用边界
公开文献只支持方法现状、基线、局限和评价设计；不得用公开文献推断本项目团队成果。每条公开命题必须绑定归档来源及哈希。""",
    )

    draft = "# 全文\n本文件定义完整申请书的章节顺序。\n\n" + "\n\n".join(
        f"# {title}\n由正式写作链基于已确认材料和归档公开证据生成。"
        for title in SECTION_TITLES
    )
    _write(output_dir / "06_current_proposal.md", draft)

    _write(
        output_dir / "07_reference_structure.md",
        """# 参考结构说明

参考申请书采用问题驱动结构：先说明场景变化与现有方法边界，再提出关键问题和可检验目标；研究内容按关系建模、影响传播、增量优化、自适应策略和实验验证展开；技术路线明确输入、状态、算法、校验和退化路径；创新点逐项绑定最接近工作、机制差异和实验；结论只总结已论证内容。

该文件只提供结构与论证方式，不提供可复制正文，不包含团队成果或目标数字。""",
    )

    roles = {
        "01_application_guide.md": "APPLICATION_GUIDE",
        "02_project_brief.md": "PROJECT_BRIEF",
        "03_method_and_evaluation.md": "EVIDENCE_MATERIAL",
        "04_foundation_and_schedule.md": "EVIDENCE_MATERIAL",
        "05_public_research_requirements.md": "EVIDENCE_MATERIAL",
        "06_current_proposal.md": "CURRENT_PROPOSAL",
        "07_reference_structure.md": "REFERENCE_PROPOSAL",
    }
    files = []
    for filename, role in roles.items():
        path = output_dir / filename
        files.append(
            {
                "filename": filename,
                "role": role,
                "security_level": "PUBLIC",
                "size_bytes": path.stat().st_size,
                "sha256": sha256_bytes(path.read_bytes()),
            }
        )
    manifest = {
        "schema_version": "1.0",
        "material_set": "G3_PUBLIC_BENCHMARK",
        "created_at": utc_now(),
        "security_level": "PUBLIC",
        "section_titles": SECTION_TITLES,
        "files": files,
        "statement": "完整公开基准材料用于真实模型、实时学术检索和完整交付链能力验收。",
    }
    (output_dir / "material_manifest.json").write_text(
        json.dumps(manifest, ensure_ascii=False, indent=2), encoding="utf-8"
    )
    return manifest


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--output-dir", type=Path, required=True)
    args = parser.parse_args()
    print(json.dumps(build(args.output_dir.resolve()), ensure_ascii=False, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
